function checkWebp() {
    try{
        return (document.createElement('canvas').toDataURL('image/webp').indexOf('data:image/webp') == 0);
    }catch(err) {
    	return false;
    }
}

function change_img(){
	if(checkWebp()==false){
	var a=$("img");
	a.each(function(a,b){
		if(b.src.slice(-4)=="webp"){
			b.src=b.src.slice(0,-1)+".png"
		}
})
}
}
